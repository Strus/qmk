bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    // TODO: fix alt tapping on Windows: https://www.reddit.com/r/olkb/comments/u3qlqz/qmk_using_modtap_only_when_another_modifier_is/i4rh870/
    switch (keycode) {
        case KC_A:
        case KC_C:
        case KC_E:
        case KC_L:
        case KC_N:
        case KC_O:
        case KC_S:
        case KC_X:
        case KC_Z: {
            os_variant_t current_os = detected_host_os();
            if (!record->event.pressed || current_os == OS_MACOS || !(get_mods() & MOD_BIT_RALT)) {
                break;
            }

            uint8_t new_mods = (get_mods() & ~MOD_BIT_RALT) | MOD_BIT_LALT;
            clear_mods();
            set_mods(new_mods);
            tap_code(keycode);
            set_mods((new_mods & ~MOD_BIT_LALT) | MOD_BIT_RALT);

            return true;
        }
        default: {
            if (!record->event.pressed || !(get_mods() & MOD_BIT_RALT)) {
                break;
            }

            uint8_t new_mods = (get_mods() & ~MOD_BIT_RALT) | MOD_BIT_LALT;
            clear_mods();
            set_mods(new_mods);
            tap_code(keycode);
            set_mods((new_mods & ~MOD_BIT_LALT) | MOD_BIT_RALT);

            return false;
        }
    }

    return true;
}

bool get_hold_on_other_key_press(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LCTL_T(KC_TAB):
        case LCTL_T(KC_ENT):
        case RSFT_T(KC_TAB):
            // Immediately select the hold action when another key is pressed.
            return true;
        default:
            // Do not select the hold action when another key is pressed.
            return false;
    }
}

uint16_t get_quick_tap_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LT(1, KC_SPC):
            return 0;
        default:
            return QUICK_TAP_TERM;
    }
}

